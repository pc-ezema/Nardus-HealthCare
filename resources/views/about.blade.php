                                        <h3>Quality Matters</h3>
                                        <p>Continuous learning through the outcomes experienced by our service users is embedded in all our systems and processes. We use Quality Assessment Framework (QAF) to enable us to self-assess the quality of our service delivery. The ultimate aim of this tool is continuous improvement and it has 5 core service objectives namely: Assessment and Support Planning; Security, Health and Safety; Safeguarding and Protection from Abuse; Fair Access, Diversity and Inclusion; Client Involvement and Empowerment. We have a specific method in place to measure the outcomes and progress made for each service user that we support. A senior member of staff meets with the service user regularly to find out outcomes achieved and/or progress being made.</p>
